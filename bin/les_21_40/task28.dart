}

// Написать функцию quot() которая возвращает
// целую часть от деления первого аргумента на второй.
// А остаток от деления выводит на экран .
// У функции два параметра — два целых числа.
// Надо делать проверку деления на ноль:
// верните первый аргумент и выведите сообщение о делении на ноль.
int quot(int a, int b) {
  if (b == 0) {
    print('деление на ноль!');
    return a;
  }
  print(a % b);
  return a ~/ b;
}

// Написать функцию dgt() которая возвращает
