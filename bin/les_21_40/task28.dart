// Написать функцию dgt2() которая возвращает одну из цифр трёхзначного числа.
// Функция принимает два аргумента — целое трёхзначное число
// (надо делать проверку, если ошибка, то верните -1 и выведите
// сообщение об ошибке на экран) и цифру-указатель разряда
// (1 - последняя цифра, 2 - цифра по середине, 3 - начальная цифра).
// Функция ничего не выводит на экран!

int dgt2(int a, int b) {
  if (a < 100 || a > 999) {
    print('число не трёхзначное!');
    return -1;
  }
  int digit = -1;
  if (b == 1) digit = a % 10;
  if (b == 2) digit = a ~/ 10 % 10;
  if (b == 3) digit = a ~/ 100;
  return digit;
}

