// Написать функцию cm2in() которая переводит длину
// из сантиметров в дюймы (1 см = 1/2,54 дюйма).
// Функция принимает один аргумент — длину в сантиметрах
// и возвращает 1 аргумент — длину в дюймах. Функция ничего не выводит на экран!
double cm2in(double cm) {
