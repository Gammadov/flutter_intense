// Написать функцию-калькулятор calc2() для двух аргументов.
// У функции три параметра. Первые два параметра — это сами числа (целые).
// Третий параметр отвечает за операцию: сложение, вычитание, умножение, деление.
// Функция возвращает и выводит на экран результат вычисления.
// Если возникает деление на ноль, функция выводит на экран
// сообщение об ошибке и возвращает 0.

double calc2(int a, int b, String c) {
  double result = 0;
  switch (c) {
    case '+':
      result = (a + b).toDouble();
      break;
    case '-':
      result = (a - b).toDouble();
      break;
    case '*':
      result = (a * b).toDouble();
      break;
    case '/':
      {
        if (b == 0) {
          print('Деление на ноль!');
          return result;
        }

        result = a / b;
      }
  }
  return result;
}

// Написать функцию mean() которая выводит n случайных чисел на экран и
// возвращает среднее арифметическое этих чисел. Функция принимает
// один аргумент — количество случайных чисел n, которые надо вывести на экран.
// n натуральное число, не превышает 10000 (надо делать проверку).
// Диапазон случайных чисел — от 0 до 999.

double mean(int n) {
  if (n < 1 || n > 10000) {
    print('n вне диапазона');
    return -1;
  }

  int sum = 0, r;
  for(int i = 0; i < n; n += 1){
    r = Random().nextInt(1000);
    print(r);
    sum += r;
  }
  return sum / n;
}


